const seedDatabase = async () => {
  try {
    // Connect to MongoDB
    const mongoUri = process.env.MONGODB_URI || 'mongodb://localhost:27017/translate_for_you';
    await mongoose.connect(mongoUri);
    console.log('✅ Connected to MongoDB');

    // Clear existing dictionary
    await Dictionary.deleteMany({});
    console.log('🗑️  Cleared existing dictionary');

    // Add programming terms
    for (const term of programmingTerms) {
      await Dictionary.updateOne(
        { english: term.english.toLowerCase() },
        {
          ...term,
          english: term.english.toLowerCase(),
          isProgramming: true
        },
        { upsert: true }
      );
    }
    console.log(`✅ Added ${programmingTerms.length} programming terms`);

    // Add common words
    for (const word of commonWords) {
      await Dictionary.updateOne(
        { english: word.english.toLowerCase() },
        {
          ...word,
          english: word.english.toLowerCase(),
          isProgramming: false
        },
        { upsert: true }
      );
    }
    console.log(`✅ Added ${commonWords.length} common words`);

    const totalWords = await Dictionary.countDocuments();
    console.log(`\n🎉 Success! Total words in dictionary: ${totalWords}`);
    
    // Show breakdown
    const programmingCount = await Dictionary.countDocuments({ isProgramming: true });
    const commonCount = await Dictionary.countDocuments({ isProgramming: false });
    
    console.log('\n📊 Breakdown:');
    console.log(`   💻 Programming terms: ${programmingCount}`);
    console.log(`   🌐 Common words: ${commonCount}`);
    
    process.exit(0);
  } catch (error) {
    console.error('❌ Error seeding database:', error);
    process.exit(1);
  }
};

seedDatabase();
